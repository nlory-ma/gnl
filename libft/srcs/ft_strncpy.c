#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t n)
{
	int		i;

	if (s1 == NULL && s2 == NULL)
		return (NULL);
	i = 0;
	while (n > 0 && s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
		n--;
	}
	while (n > 0)
	{
		s1[i] = '\0';
		i++;
		n--;
	}
	return (s1);
}
