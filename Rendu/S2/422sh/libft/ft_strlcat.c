#include "includes/libft.h"

size_t			ft_strlcat(char *s1, const char *s2, size_t n)
{
	size_t			i;
	size_t			j;

	i = 0;
	j = 0;
	while (*s1++)
		i++;
	while (n-- && s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (i - 1);
}
